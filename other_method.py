
# copy() method -> The copy() method returns a copy of the specified list.

fruits = ["apple", "banana", "cherry"]

x = fruits.copy()

print(x)

# reverse() method -> The reverse() method reverses the sorting order of the elements.

fruits = ['apple', 'banana', 'cherry']

fruits.reverse()

print(fruits)

# sort() method -> The sort() method sorts the list ascending by default.

cars = ['Ford', 'BMW', 'Volvo']

cars.sort()

print(cars)
